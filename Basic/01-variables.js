// var

var helloWorld = "Hello, JavaScript!";
console.log(helloWorld);

helloWorld = "Hello de nuevo, JavaScript!";
console.log(helloWorld);

// let = Cuando se usa let, la variable puede ser reasignada, pero no redeclarada en el mismo ámbito.

let helloWorld2 = "Hello, JavaScript!";
console.log(helloWorld2);

helloWorld2 = "Hello de nuevo, JavaScript!";
console.log(helloWorld2);

// const = Cuando se usa const, la variable no puede ser reasignada ni redeclarada en el mismo ámbito.

const helloWorld3 = "Hello, JavaScript!";
console.log(helloWorld3);

